#Escribe un programa que solicite al usuario una palabra y luego muestre la palabra sin los caracteres repetidos.

palabra = input('Ingrese una palabra: ')
conjunto = set()

for i in range(1,len(palabra)+1):
    conjunto.add(palabra[i-1:i])

print(conjunto)


